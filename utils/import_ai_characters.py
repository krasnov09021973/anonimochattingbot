def import_characters():
    conn = sqlite3.connect(DB_PATH)
    cursor = conn.cursor()

    # Очищаем
    cursor.execute('DELETE FROM ai_characters')

    for char in CHARACTERS:
        gender, topic, traits, style, rules, names_json = char
        cursor.execute('''
            INSERT INTO ai_characters (gender, topic, traits, style, rules, names, min_age, max_age, is_active)
            VALUES (?, ?, ?, ?, ?, ?, 18, 30, 1)
        ''', (gender, topic, traits, style, rules, names_json))

    conn.commit()
    conn.close()
    print(f"✅ Импортировано персонажей: {len(CHARACTERS)}")

if __name__ == "__main__":
    import_characters()
